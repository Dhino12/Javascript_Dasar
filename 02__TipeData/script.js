
// ==========================
// undefined
// Tipe data ini terbentuk ketika sebuah variabel tidak memiliki nilai
// ==========================

let x;
console.log(x);
console.log(typeof(x));
// Fungsi typeof() digunakan untuk memastikan tipe data
    //  pada variabel dengan mengembalikan tipe data tersebut dalam bentuk teks
    
// =================================================================

// ==========================
// Numbers
// Nilai dari tipe data number adalah angka
// ==========================
let ex = 10;
console.log(ex + " tipeData = " + typeof(ex)); 
// =================================================================

// ==========================
// Strings
// String ini dasarnya adalah sebuah teks
// ==========================
let greet = "Haii";
let nama = 'latina';
console.log(greet + " tipeData = " + typeof(greet)); 
console.log(nama + " tipeData = " + typeof(nama)); 
// =================================================================

// ==========================
// Boolean
// Boolean hanya dapat memiliki dua nilai, yakni true atau false.
    // Tipe data ini menjadi kunci utama dalam penentuan logika, 
// ==========================
let boolX = false; 
let boolY = true; 
console.log(boolX + " tipeData = " + typeof(boolX)); 
console.log(boolY + " tipeData = " + typeof(boolY)); 
// =================================================================

// ==========================
// null
// null biasa digunakan sebagai nilai sementara pada variabel, 
    // tapi sebenarnya nilai tersebut “tidak ada”.
// ==========================
let someLaterData = null;  
console.log(someLaterData + " tipeData = " + typeof(someLaterData));  
// =================================================================